/* global $ */

(function main () {
  /**
   * Additione deux nombres entre eux
   * @param {number} numberA
   * @param {number} numberB
   * @return {number} resultat
   */
  function add (numberA, numberB) {
    return numberA + numberB
  }

  /**
   * Soustraire deux nombres entre eux
   * @param  {number} numberA
   * @param  {number} numberB
   * @return {number} resultat
   */
  function sub (numberA, numberB) {
    return numberA - numberB
  }

  /**
   * Multiplie deux nombres entre eux
   * @param  {number} numberA
   * @param  {number} numberB
   * @return {number} resultat
   */
  function multiply (numberA, numberB) {
    return numberA * numberB
  }

  /**
   * Divise deux nombres entre eux
   * @param  {number} numberA
   * @param  {number} numberB
   * @return {number} resultat
   */
  function divise (numberA, numberB) {
    return numberA / numberB
  }
})()
